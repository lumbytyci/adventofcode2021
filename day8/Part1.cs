        public static int RunPart(string input)
        {
            var lengths = new List<int>() { 2, 3, 4, 7 };
            return input.Split("\r\n")
                .Select(x => x.Split("|")[1].Trim())
                .Select(x => x.Split(" "))
                .Select(x => x.ToList().Count(x => lengths.Contains(x.Length)))
                .Sum();
        }
